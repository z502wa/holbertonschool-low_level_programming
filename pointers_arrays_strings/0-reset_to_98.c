#include "main.h"

/**
 * reset_to_98 - Updates the value of an integer to 98 using a pointer.
 * @n: Pointer to an integer whose value will be updated.
 *
 * Description: This function takes a pointer to an integer as a parameter
 * and modifies the value stored at the memory address to 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
